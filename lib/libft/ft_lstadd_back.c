#include "libft.h"

void	ft_lstadd_back(t_node **lst, t_node *new)
{
	t_node	*aux;

	if (!new)
		return ;
	if (!*lst)
	{
		*lst = new;
		return ;
	}
	aux = ft_lstlast(*lst);
	aux->next = new;
}
